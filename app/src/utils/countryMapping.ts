// تابع برای دریافت کد کشور
export const getCountryCode = (range: number): string => {
  return countryCodeMapping[range.toString()] || 'XX';
};

// تابع برای دریافت URL پرچم
export const getFlagUrl = (range: number): string => {
  const countryCode = getCountryCode(range);
  return `https://countryflagsapi.netlify.app/flag/${countryCode.toLowerCase()}.svg`;
};
